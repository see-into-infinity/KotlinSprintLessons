package org.example.lesson_2

fun main() {
    val firstStudentMark : Float = 3.0f
    val secondStudentMark : Float = 4.0f
    val thirdStudentMark : Float = 3.0f
    val fourthStudentMark : Float = 5.0f
    var amountOfStudents = 4

    var arithmeticMean : Float = (firstStudentMark + secondStudentMark + thirdStudentMark + fourthStudentMark) / amountOfStudents

    println(arithmeticMean)
}